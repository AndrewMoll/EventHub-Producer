        //used for reating random credit card numbers
        public static string ALPHA_NUM = "0123456709abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUV";
        public static Random rand = new Random(Thread.CurrentThread.ManagedThreadId);

        public static async void Run(List<EventHubSender> sender)
        {
            //Create a dictonary for each data type that has unique vaules or constraints.  Populate the arrays with the correct data.  
            //Geo and credit card do not need arrays as they are bound 
            Dictionary<int, double> products = new Dictionary<int, double>();
            List<string> names = new List<string>();
            products = addAllProducts(products);
            names = addAllNames(names);
            int counter = 0;
            int y = 0;
            int senderCounter = 0;
            while (y < 1)
            {
                Thread.Sleep(10);
                if(counter % 1000 ==0 )
                { Console.WriteLine(counter); }
                //Put all data into data object for eazy serilization
                DataObject obj = new DataObject { CreditCardNumber = getCreditCardInfo(5), GeographyId = rand.Next(1, 5), FirstName = names[rand.Next(0, names.Count)] };
                double value = products[rand.Next(1, products.Count)];
                int key = products.FirstOrDefault(x => x.Value == value).Key;
                obj.ItemId = key;
                obj.ItemPrice = value;
                //send event using the sender associated to the correct partition
                SendEvent(obj,sender[senderCounter]);
                counter = counter + 1;

                //make sure we don't go over the number of senders assigned to this task
                if(senderCounter == sender.Count-1)
                {
                    senderCounter = 0;
                }
                else { senderCounter = senderCounter + 1; }
            }


        }




        //Get a random alpha numeric string of the length supplied
        private static string getCreditCardInfo(int len)
    {
              
        StringBuilder sb = new StringBuilder(len);
        for (int i = 0; i < len; i++)
        {
             
            int ndx = (int)(rand.NextDouble() * ALPHA_NUM.Length);
            sb.Append(ALPHA_NUM.ElementAt(ndx));
        }
        return sb.ToString();
    }

        //send serilized event to the event hub
    static  void SendEvent(DataObject data, EventHubSender sender)
    {     
        var serializedString = JsonConvert.SerializeObject(data);
        sender.SendAsync(new EventData(Encoding.UTF8.GetBytes(serializedString)));
    }



